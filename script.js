document.addEventListener('DOMContentLoaded', () => {
    
    function getStorage(key, def) {
        try {
            return localStorage.getItem(key) || def;
        } catch (e) {
            console.warn('LocalStorage access denied', e);
            return def;
        }
    }
    
    function setStorage(key, val) {
        try {
            localStorage.setItem(key, val);
        } catch (e) {
            console.warn('LocalStorage access denied', e);
        }
    }

    const navButtons = document.querySelectorAll('.nav-btn');
    const windows = document.querySelectorAll('.game-window');
    const closeButtons = document.querySelectorAll('.close-btn');
    var cursorGlow = document.getElementById('cursor-glow'); 
    const typingElement = document.getElementById('typing-text');
    
    const themeToggle = document.getElementById('theme-toggle');
    const langToggle = document.getElementById('lang-toggle');

    let currentWindow = null;
    let currentLang = getStorage('lang', 'ar'); 
    let currentTheme = getStorage('theme', 'dark');

    try {
        applyTheme(currentTheme);
        applyLanguage(currentLang);
    } catch (e) {
        console.error("Init error:", e);
    }


    function applyTheme(theme) {
        currentTheme = theme;
        setStorage('theme', theme);
        
        if (theme === 'light') {
            document.documentElement.setAttribute('data-theme', 'light');
            if(themeToggle) themeToggle.innerHTML = '<i class="fas fa-moon"></i>';
        } else {
            document.documentElement.setAttribute('data-theme', 'dark');
            if(themeToggle) themeToggle.innerHTML = '<i class="fas fa-sun"></i>';
        }
    }

    function applyLanguage(lang) {
        currentLang = lang;
        setStorage('lang', lang);
        
        const isRtl = lang === 'ar';
        document.documentElement.lang = lang;
        document.documentElement.dir = isRtl ? 'rtl' : 'ltr';
        
        if(langToggle) langToggle.textContent = lang === 'ar' ? 'EN' : 'عربي';
        
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                 if (translations[lang][key].includes('**') || translations[lang][key].includes('<')) {
                    el.innerHTML = translations[lang][key].replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>');
                } else {
                    el.textContent = translations[lang][key];
                }
            }
        });
        
        resetTypingEffect();
    }
    

    function openWindow(targetId) {
        if (targetId === 'home') {
            closeAllWindows();
            return;
        }

        const targetWindow = document.getElementById(targetId);
        if (!targetWindow) return;

        if (currentWindow === targetWindow) return;

        if (currentWindow) {
            closeWindow(currentWindow);
        }

        targetWindow.classList.remove('hidden');
        requestAnimationFrame(() => {
            targetWindow.classList.add('active');
        });
        
        currentWindow = targetWindow;
        updateNavState(targetId);
        
        if (targetId === 'window-skills') {
            setTimeout(animateSkillBars, 300);
        }
    }

    function closeWindow(windowElement) {
        windowElement.classList.remove('active');
        windowElement.classList.add('closing');
        
        setTimeout(() => {
            windowElement.classList.remove('closing');
            windowElement.classList.add('hidden');
        }, 300); 
        
        if (currentWindow === windowElement) {
            currentWindow = null;
            updateNavState('home');
        }
    }

    function closeAllWindows() {
        if (currentWindow) {
            closeWindow(currentWindow);
        }
    }

    function updateNavState(activeId) {
        navButtons.forEach(btn => {
            btn.classList.remove('active');
            if (btn.dataset.target === activeId) {
                btn.classList.add('active');
            }
        });
        
        if(!currentWindow && activeId !== 'home') {
             const homeBtn = document.querySelector('[data-target="home"]');
             if(homeBtn) homeBtn.classList.add('active');
        }
    }

    function animateSkillBars() {
        const bars = document.querySelectorAll('#window-skills .skill-progress-bar');
        bars.forEach(bar => {
            bar.style.width = '0%';
            setTimeout(() => {
                const parent = bar.parentElement.parentElement;
                const progress = parent.getAttribute('data-progress');
                if(progress) bar.style.width = progress + '%';
            }, 50);
        });
    }

    let typingInterval; 
    let typingTimeout;
    
    function resetTypingEffect() {
        if (typingInterval) clearInterval(typingInterval);
        if (typingTimeout) clearTimeout(typingTimeout);
        
        const textsAr = ["الذكاء الاصطناعي", "التعلم العميق", "رؤية الكمبيوتر", "تحليل البيانات"];
        const textsEn = ["Artificial Intelligence", "Deep Learning", "Computer Vision", "Data Analysis"];
        
        const texts = currentLang === 'ar' ? textsAr : textsEn;
        
        let textIndex = 0;
        let charIndex = 0;
        let isDeleting = false;
        
        const typeElem = document.getElementById('typing-text');
        if (!typeElem) return;

        function type() {
            const fullText = texts[textIndex];
            
            if (isDeleting) {
                charIndex--;
            } else {
                charIndex++;
            }

            typeElem.textContent = fullText.substring(0, charIndex);

            let typeSpeed = isDeleting ? 50 : 100;

            if (!isDeleting && charIndex === fullText.length) {
                typeSpeed = 2000; 
                isDeleting = true;
            } else if (isDeleting && charIndex === 0) {
                isDeleting = false;
                textIndex = (textIndex + 1) % texts.length;
                typeSpeed = 500; 
            }

            typingTimeout = setTimeout(type, typeSpeed);
        }
        
        type();
    }


    if(themeToggle) {
        themeToggle.addEventListener('click', () => {
            const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
            applyTheme(newTheme);
        });
    }

    if(langToggle) {
        langToggle.addEventListener('click', () => {
            const newLang = currentLang === 'ar' ? 'en' : 'ar';
            applyLanguage(newLang);
        });
    }

    navButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            const target = btn.dataset.target;
            openWindow(target);
        });
    });

    closeButtons.forEach(btn => {
        btn.addEventListener('click', (e) => {
            const win = e.target.closest('.game-window');
            if (win) closeWindow(win);
        });
    });

    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape') closeAllWindows();
    });

    document.addEventListener('mousemove', (e) => {
        if (cursorGlow) {
            cursorGlow.style.left = e.clientX + 'px';
            cursorGlow.style.top = e.clientY + 'px';
        }
    });

    document.addEventListener('mousedown', () => cursorGlow && cursorGlow.classList.add('active-click'));
    document.addEventListener('mouseup', () => cursorGlow && cursorGlow.classList.remove('active-click'));

    updateNavState('home');
});
